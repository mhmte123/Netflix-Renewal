// 카테고리 id → 한글 이름 매핑 (문의 내역 등에서 사용)
export const FAQ_CATEGORY_LABELS: Record<string, string> = FAQ_CATEGORIES.reduce(
  (acc, c) => ({ ...acc, [c.id]: c.name }),
  { other: "기타" } as Record<string, string>,
);

// 특정 카테고리의 질문 목록을 꺼내는 헬퍼
export const getFaqItems = (categoryId: string): FaqItem[] =>
  FAQ_CATEGORIES.find((c) => c.id === categoryId)?.items ?? [];

// 모든 질문을 평탄화 (전체 검색/모아보기용)
export const ALL_FAQ_ITEMS: (FaqItem & { categoryId: string; categoryName: string })[] =
  FAQ_CATEGORIES.flatMap((c) =>
    c.items.map((item) => ({ ...item, categoryId: c.id, categoryName: c.name })),
  );